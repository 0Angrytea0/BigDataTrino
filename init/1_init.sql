CREATE DATABASE IF NOT EXISTS mydb;
CREATE TABLE IF NOT EXISTS mydb.sales (
    id                       UInt64,
    customer_first_name      String,
    customer_last_name       String,
    customer_age             UInt8,
    customer_email           String,
    customer_country         String,
    customer_postal_code     String,
    customer_pet_type        String,
    customer_pet_name        String,
    customer_pet_breed       String,
    seller_first_name        String,
    seller_last_name         String,
    seller_email             String,
    seller_country           String,
    seller_postal_code       String,
    product_name             String,
    product_category         String,
    product_price            Float32,
    product_quantity         UInt32,
    sale_date                DateTime,
    sale_customer_id         UInt64,
    sale_seller_id           UInt64,
    sale_product_id          UInt64,
    sale_quantity            UInt32,
    sale_total_price         Float32,
    store_name               String,
    store_location           String,
    store_city               String,
    store_state              String,
    store_country            String,
    store_phone              String,
    store_email              String,
    pet_category             String,
    product_weight           Float32,
    product_color            String,
    product_size             String,
    product_brand            String,
    product_material         String,
    product_description      String,
    product_rating           Float32,
    product_reviews          UInt32,
    product_release_date     DateTime,
    product_expiry_date      DateTime,
    supplier_name            String,
    supplier_contact         String,
    supplier_email           String,
    supplier_phone           String,
    supplier_address         String,
    supplier_city            String,
    supplier_country         String
) ENGINE = MergeTree()
ORDER BY id;
